# –•—Ä–∞–Ω–∏–ª–∏—â–µ
users = {}

async def start(update: Update, context):
    user_id = update.effective_user.id
    users[user_id] = {'score': 0, 'question': 0}
    
    await update.message.reply_text(
        "üîπ –û–±–µ—Ä–∏ –æ–¥–Ω—É –≤—ñ–¥–ø–æ–≤—ñ–¥—å, —è–∫–∞ –Ω–∞–π–±—ñ–ª—å—à–µ —Ç–æ–±—ñ –≤—ñ–¥–≥—É–∫—É—î—Ç—å—Å—è.\n"
        "üîπ –í –∫—ñ–Ω—Ü—ñ –±—É–¥–µ —Ä–æ–∑—à–∏—Ñ—Ä–æ–≤–∫–∞ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—É."
    )
    
    await ask_question(update, user_id)

async def ask_question(update: Update, user_id: int):
    if user_id not in users:
        return
    
    data = users[user_id]
    
    if data['question'] < len(QUESTIONS):
        keyboard = [['A', 'B'], ['C', 'D']]
        reply_markup = ReplyKeyboardMarkup(keyboard, resize_keyboard=True)
        await update.message.reply_text(QUESTIONS[data['question']], reply_markup=reply_markup)
    else:
        await show_result(update, user_id)

async def handle_message(update: Update, context):
    user_id = update.effective_user.id
    
    if user_id not in users:
        await update.message.reply_text("–ù–∞–ø–∏—à–∏ /start")
        return
    
    answer = update.message.text.upper()
    
    if answer in ['A', 'B', 'C', 'D']:
        scores = {'A': 1, 'B': 2, 'C': 3, 'D': 4}
        users[user_id]['score'] += scores[answer]
        users[user_id]['question'] += 1
        
        await ask_question(update, user_id)
    else:
        await update.message.reply_text("–û–±–µ—Ä–∏ A, B, C –∞–±–æ D")

async def show_result(update: Update, user_id: int):
    score = users[user_id]['score']
    
    result_text = ""
    for (min_s, max_s), text in RESULTS.items():
        if min_s <= score <= max_s:
            result_text = text
            break
    
    await update.message.reply_text(
        f"üìä –¢–≤—ñ–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç: {score} –±–∞–ª—ñ–≤\n\n{result_text}\n\n"
        f"–©–æ–± –ø—Ä–æ–π—Ç–∏ —Ç–µ—Å—Ç –∑–Ω–æ–≤—É, –Ω–∞–ø–∏—à–∏ /start"
    )
    
    del users[user_id]

def main():
    # –°–æ–∑–¥–∞–µ–º –±–æ—Ç–∞
    app = Application.builder().token(TOKEN).build()
    
    # –î–æ–±–∞–≤–ª—è–µ–º –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–∏
    app.add_handler(CommandHandler("start", start))
    app.add_handler(MessageHandler(filters.TEXT & ~filters.COMMAND, handle_message))
    
    # –ó–∞–ø—É—Å–∫–∞–µ–º
    logger.info("ü§ñ –ë–æ—Ç –∑–∞–ø—É—Å–∫–∞—î—Ç—å—Å—è...")
    app.run_polling()

if __name__ == '__main__':
    main()
